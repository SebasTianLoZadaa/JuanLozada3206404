import random

def buscar_numero():
    lista = [random.randint(1, 100) for _ in range(20)]
    print("Lista:", lista)
    objetivo = int(input("Ingrese el número a buscar: "))
    if objetivo in lista:
        print("Número encontrado en la posición:", lista.index(objetivo))
    else:
        print("Número no encontrado")

buscar_numero()